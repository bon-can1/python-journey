# Execute code when a condition is true.
# if runs a block of code only when the condition is True

temperature = 35

if temperature > 30:
    print("It is a hot day!")
    print("Stay hydrated.")

# another example
score = 85

if score >= 50:
    print("You passed the exam.")

# if with a False condition — nothing prints
age = 15

if age >= 18:
    print("You can vote.")

print("Program continues after the if block.")

