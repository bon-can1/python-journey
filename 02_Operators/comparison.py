# Compare values and return True or False.
# comparison operators are used in conditions (if statements)

a = 10
b = 20
c = 10

# equal to — symbol "=="
print("a == b:", a == b)   # False
print("a == c:", a == c)   # True

# not equal to — symbol "!="
print("a != b:", a != b)   # True
print("a != c:", a != c)   # False

# greater than — symbol ">"
print("a > b:", a > b)     # False
print("b > a:", b > a)     # True

# less than — symbol "<"
print("a < b:", a < b)     # True

# greater than or equal to — symbol ">="
print("a >= c:", a >= c)   # True

# less than or equal to — symbol "<="
print("a <= b:", a <= b)  # True

# comparing strings (alphabetical order)
name1 = "Arun"
name2 = "Bob"
print(f'"{name1}" < "{name2}":', name1 < name2)
