# Perform mathematical calculations.
# arithmetic operators work on numbers (int and float)

a = 10
b = 3

# addition — symbol "+"
print("Addition:", a + b)

# subtraction — symbol "-"
print("Subtraction:", a - b)

# multiplication — symbol "*"
print("Multiplication:", a * b)

# division — symbol "/" (always returns a float)
print("Division:", a / b)

# modulus — symbol "%" (remainder after division)
print("Modulus:", a % b)

# exponentiation — symbol "**" (power)
print("Exponentiation:", a ** b)

# floor division — symbol "//" (whole number division, drops decimal)
print("Floor division:", a // b)

# examples with floats
x = 7.5
y = 2
print(f"{x} / {y} =", x / y)
print(f"{x} // {y} =", x // y)
