# Assign and update variable values.
# assignment operators combine math with assignment

x = 10
print("x =", x)

# simple assignment — symbol "="
x = 5
print("x =", x)

# add and assign — symbol "+="
x += 3   # same as x = x + 3
print("x += 3:", x)

# subtract and assign — symbol "-="
x -= 2   # same as x = x - 2
print("x -= 2:", x)

# multiply and assign — symbol "*="
x *= 4   # same as x = x * 4
print("x *= 4:", x)

# divide and assign — symbol "/="
x /= 2   # same as x = x / 2
print("x /= 2:", x)

# floor divide and assign — symbol "//="
x //= 2
print("x //= 2:", x)

# modulus and assign — symbol "%="
x = 17
x %= 5   # same as x = x % 5
print("x %= 5:", x)

# exponent and assign — symbol "**="
x = 2
x **= 3  # same as x = x ** 3
print("x **= 3:", x)
