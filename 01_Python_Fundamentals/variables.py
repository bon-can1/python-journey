# Store and manage data using variables.
# A variable is a name that holds a value

# rules for variable names:
#   -> cannot start with a number or special character
#   -> can start with a letter or underscore (_)
#   -> can use letters, numbers, and underscore
#   -> uppercase and lowercase are different (name and Name are not the same)

name = "Arun"       # string variable
age = 20            # integer variable
height = 5.8        # float variable
is_learning = True  # boolean variable

print(name)
print(age)
print(height)
print(is_learning)

# you can print all variables in one line
print(name, age, height, is_learning)

# you can change a variable's value anytime
age = 21
print("Updated age:", age)
