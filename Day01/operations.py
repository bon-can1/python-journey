# there few operations in python that we can perform on variables 
#       that can be used to perform mathematical operations
#       these operations are known as arithmetic operations
#       addition, subtraction, multiplication, division, modulus, exponentiation, floor division

# addition is done using the + operator

a = 10 # a is a variable that is assigned the value 10 and it is an integer
b = 20 # b is a variable that is assigned the value 20 and it is an integer
c = a + b # c is a variable that is assigned the value of a + b and it is an integer
print("the value of c is", c) # print the value of c
# there is few ways to print the value of a + b
print(f"the value of a + b is {a + b}") # print the value of a + b 

# substraction 
c = 12
