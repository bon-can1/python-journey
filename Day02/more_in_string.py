name = "python journey"

print(len(name))   # find the lenght of the line "space also count"
print(name.find("o")) # find the letter "o" from the beginning in text
print(name.capitalize())  # the first letter from the text will became capital 
print(name.upper()) # make all letter in the text capital or uppercase
print(name.lower()) # make all letter in text small letter or lowercase
print(name.isdigit())  # check is there any digit in the data we store
print(name.isalpha())  # check is there any alphabet in the data we store
print(name.replace("o", "e")) # the letter the we take 1st will replace with the second letter we give in every "o" from the text
print(name * 3) # multiplication in str